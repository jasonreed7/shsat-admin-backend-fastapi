from enum import Enum


class QuestionType(Enum):
    MULTIPLE_CHOICE = "MULTIPLE_CHOICE"
    FILL_IN = "FILL_IN"
    MULTIPLE_CHOICE_IMAGE = "MULTIPLE_CHOICE_IMAGE"
    FILL_IN_IMAGE = "FILL_IN_IMAGE"


class UsageType(Enum):
    OFFICIAL_TEST_QUESTION = "OFFICIAL_TEST_QUESTION"
    TEST_QUESTION = "TEST_QUESTION"
    PROBLEM_SET_QUESTION = "PROBLEM_SET_QUESTION"


class QuestionOrAnswer(str, Enum):
    QUESTION = "question"
    ANSWER = "answer"


class PassageType(Enum):
    IMAGE = "IMAGE"
    TEXT = "TEXT"


class Section(Enum):
    MATH = "MATH"
    ENGLISH = "ENGLISH"


class SubSection(Enum):
    REVISING_EDITING = "REVISING_EDITING"
    READING_COMPREHENSION = "READING_COMPREHENSION"
    MATH_FILL_IN = "MATH_FILL_IN"
    MATH_MULTIPLE_CHOICE = "MATH_MULTIPLE_CHOICE"
